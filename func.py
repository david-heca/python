# Funciones en Python

print('Hola') # Es una finción que recibe un argumento

def my_print(text): # Estamos creando una función la cual necesita un parametro
    print(text * 2)

my_print('hoola') # Podemos volver a usar las funciones cuantas veces queramos
 
def suma(a, b): # En esta función estamos declarando que necesitamos dos parametros
    print(a + b)

suma(5, 5) # Aquí asignamos los valores a la función
suma(10, 34)