# Tipos de datos en Python

nombre = "David" # String o cadena de texto
print("nombre: ", nombre)

print(type(nombre)) # Nos muestra el tipo de dato de una variable

edad = 22 # Integer o números
print(edad)
print(type(edad))

soltero = True # Boolean
print(soltero)
print(type(soltero))

edad = input("¿Cuál es tu edad? \n") # Los input devuelven un String siempre
print(edad)
print(type(edad))