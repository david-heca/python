# Operadores de comparación en Python

# Los operadores devuelven True o False
# > Mayor que
print(7 > 3)

# < Menor que
print(8 < 10)

# >= Mayor o igual
print(2 >= 2)

# <= Menor o igual
print(3 <= 10)

# == Igualdad
print(2 == 4)

# != Desigualdad o diferente de
print(9 != 10)

print("Apple" == "apple") # También se puede hacer con cadenas de texto
print(1 == "1")