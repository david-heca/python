# Funciones en Python

print('Hola') # Print es una función como tal

def my_print(text): # Creamos una funcion que recibe un parámetro
    print(text * 2)

my_print('hola') # Enviamos un argumento a la función my_print
 
def suma(a, b): # En esta función estamos declarando que necesitamos dos parámetros
    print(a + b)

suma(5, 5)
